#!/usr/bin/env node
// Refresh shared/cdn.json from the live clf CDN.
//
// Fetches the CDN's manifest.json, constructs the eight oister-shell
// asset URLs from its version paths, fetches each asset to compute
// its SRI (sha384), and writes shared/cdn.json. Run this after a
// clf-core / clf-factory release so generated shells pin current
// URLs + integrity (the version paths change every release, so a
// hand-edited manifest would drift).
//
// Usage:
//   node bin/sync-cdn.mjs [--base https://cdn.blindhub.ca]

import { createHash } from "node:crypto";
import { writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { parseArgs } from "node:util";

import { cdnUrlsFromManifest, readJson } from "../src/lib.mjs";

const PKG_DIR = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const log = (m) => process.stdout.write(`▸ ${m}\n`);

const { values } = parseArgs({ options: { base: { type: "string" } } });
const config = readJson(join(PKG_DIR, "generator.config.json"));
const base = (values.base || config.cdnBase || "https://cdn.blindhub.ca")
  .replace(/\/$/, "");

async function fetchBytes(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`GET ${url} -> ${res.status} ${res.statusText}`);
  return new Uint8Array(await res.arrayBuffer());
}

const manifest = JSON.parse(
  new TextDecoder().decode(await fetchBytes(`${base}/manifest.json`)));
log(`cobd-cdn ${manifest.version} @ ${base}`);

const urls = cdnUrlsFromManifest(manifest, base);
const out = {
  $comment: `Generated by bin/sync-cdn.mjs from ${base}/manifest.json `
    + `(cobd-cdn ${manifest.version}). URLs + SRI for the oister shell `
    + `assets; re-run \`npm run sync-cdn\` after a clf release to refresh.`,
};
for (const [asset, { url }] of Object.entries(urls)) {
  const bytes = await fetchBytes(url);
  const sha = createHash("sha384").update(bytes).digest("base64");
  out[asset] = {
    url,
    integrityAttr: `integrity="sha384-${sha}" crossorigin="anonymous"`,
  };
  log(`${asset} -> ${url}`);
}

const dest = join(PKG_DIR, config.cdnManifest);
writeFileSync(dest, JSON.stringify(out, null, 2) + "\n");
log(`wrote ${config.cdnManifest} (cobd-cdn ${manifest.version})`);
